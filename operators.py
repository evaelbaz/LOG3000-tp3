def add(a,b):
    """
    This function does a basic addition between two floats

    Args:
        a (float): first float to add
        b (float): second float to add

    Returns:
        float: result of the addition
    """
    return a + b

def subtract(a,b):
    return b - a

def multiply(a,b):
    return a ** b

def divide(a,b):
    return a // b
