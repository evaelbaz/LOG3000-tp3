def add(a,b):
    """Cette fonction prend a et b en paramètres et calcule le résultat de leur addition."""
    return a + b

def subtract(a,b):
    """Cette fonction prend a et b en paramètres et calcule le résultat de la soustraction de b
    à a."""

    return b - a

def multiply(a,b):
    """Cette fonction prend a et b en paramètres et calcule le résultat de leur multiplication."""
    return a ** b

def divide(a,b):
    """Cette fonction prend a et b en paramètre et calcule le résultat de la division de a par b."""

    return a // b
